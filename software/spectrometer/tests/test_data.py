def test_get_pipe():
    # Arrange
    fid_direct = dat.FID1D(
        data=sample_data,
        spectral_width=50e3,
        carrier_freq=12_442.418975830078,
        label="C13",
        observation_freq=125_680_999.75585938,
        sample="Unknown Sample",
        pulse="unknown.fid",
        spectrometer="Unknown Spectrometer",
        timestamp=dt.datetime(
            year=2012, month=11, day=29, hour=11, minute=33, second=33, tzinfo=dt.UTC
        ),
    )

    # Act
    pipedic = fid_direct._get_pipedic()

    # Assert
    float_keylist = [k for k, v in sample_pipedic.items() if isinstance(v, float)]
    other_keylist = [k for k, v in sample_pipedic.items() if not isinstance(v, float)]

    assert {k: pipedic[k] for k in other_keylist} == {
        k: sample_pipedic[k] for k in other_keylist
    }
    nptest.assert_allclose(
        np.array([pipedic[k] for k in float_keylist]),
        np.array([sample_pipedic[k] for k in float_keylist]),
    )


